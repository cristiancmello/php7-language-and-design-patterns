/*
Pseudo-tipos e variáveis usadas em documentações oficiais em PHP

 * mixed: indica que um parâmetro pode aceitar vários tipos (não necessariamente todos).
          Em resumo, para > 2 tipos se costuma usar mixed

          Exemplo: `gettype` aceita todos os tipos do PHP

 * number: number indica que um parâmetro pode ser tanto [integer | float]

 * callable: pseudo-tipo que significa o mesmo que callback antigamente

 * [array | object]: indica que o parâmetro pode ser tanto array quanto object

 * void: indica que não há valor a ser retornado. Numa lista de parâmetros,
         indica que a função não aceita parâmetros.

 * $...: indica 'assim por diante'. É equivalente a lista variável de argumentos
         em algumas linguagens, como C/C++.
 */
